package TY;

public class TYMarks {
    private double theory;
    private double practicals;

    public TYMarks(double theory, double practicals) {
        this.theory = theory;
        this.practicals = practicals;
    }

    public double getTheory() {
        return theory;
    }

    public double getPracticals() {
        return practicals;
    }
}
